import { AppBlock, events } from "@slflows/sdk/v1";
import { AthenaClient, ListSessionsCommand } from "@aws-sdk/client-athena";
import { STSClient, AssumeRoleCommand } from "@aws-sdk/client-sts";

const listSessions: AppBlock = {
  name: "List Sessions",
  description: `Lists the sessions in a workgroup that are in an active state like CREATING, CREATED, IDLE, or BUSY.`,
  inputs: {
    default: {
      config: {
        region: {
          name: "Region",
          description: "AWS region for this operation",
          type: "string",
          required: true,
        },
        assumeRoleArn: {
          name: "Assume Role ARN",
          description:
            "Optional IAM role ARN to assume before executing this operation. If provided, the block will use STS to assume this role and use the temporary credentials.",
          type: "string",
          required: false,
        },
        WorkGroup: {
          name: "Work Group",
          description: "The workgroup to which the session belongs.",
          type: "string",
          required: true,
        },
        StateFilter: {
          name: "State Filter",
          description: "A filter for a specific session state.",
          type: "string",
          required: false,
        },
        MaxResults: {
          name: "Max Results",
          description: "The maximum number of sessions to return.",
          type: "number",
          required: false,
        },
        NextToken: {
          name: "Next Token",
          description:
            "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.",
          type: "string",
          required: false,
        },
      },
      onEvent: async (input) => {
        const { region, assumeRoleArn, ...commandInput } =
          input.event.inputConfig;

        let credentials = {
          accessKeyId: input.app.config.accessKeyId,
          secretAccessKey: input.app.config.secretAccessKey,
          sessionToken: input.app.config.sessionToken,
        };

        // Determine credentials to use
        if (assumeRoleArn) {
          // Use STS to assume the specified role
          const stsClient = new STSClient({
            region: region,
            credentials: credentials,
            ...(input.app.config.endpoint && {
              endpoint: input.app.config.endpoint,
            }),
          });

          const assumeRoleCommand = new AssumeRoleCommand({
            RoleArn: assumeRoleArn,
            RoleSessionName: `flows-session-${Date.now()}`,
          });

          const assumeRoleResponse = await stsClient.send(assumeRoleCommand);
          credentials = {
            accessKeyId: assumeRoleResponse.Credentials!.AccessKeyId!,
            secretAccessKey: assumeRoleResponse.Credentials!.SecretAccessKey!,
            sessionToken: assumeRoleResponse.Credentials!.SessionToken!,
          };
        }

        const client = new AthenaClient({
          region: region,
          credentials: credentials,
          ...(input.app.config.endpoint && {
            endpoint: input.app.config.endpoint,
          }),
        });

        const command = new ListSessionsCommand(commandInput as any);
        const response = await client.send(command);

        await events.emit(response || {});
      },
    },
  },
  outputs: {
    default: {
      name: "List Sessions Result",
      description: "Result from ListSessions operation",
      possiblePrimaryParents: ["default"],
      type: {
        type: "object",
        properties: {
          NextToken: {
            type: "string",
            description:
              "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.",
          },
          Sessions: {
            type: "array",
            items: {
              type: "object",
              properties: {
                SessionId: {
                  type: "string",
                },
                Description: {
                  type: "string",
                },
                EngineVersion: {
                  type: "object",
                  properties: {
                    SelectedEngineVersion: {
                      type: "string",
                    },
                    EffectiveEngineVersion: {
                      type: "string",
                    },
                  },
                  additionalProperties: false,
                },
                NotebookVersion: {
                  type: "string",
                },
                Status: {
                  type: "object",
                  properties: {
                    StartDateTime: {
                      type: "string",
                    },
                    LastModifiedDateTime: {
                      type: "string",
                    },
                    EndDateTime: {
                      type: "string",
                    },
                    IdleSinceDateTime: {
                      type: "string",
                    },
                    State: {
                      type: "string",
                    },
                    StateChangeReason: {
                      type: "string",
                    },
                  },
                  additionalProperties: false,
                },
              },
              additionalProperties: false,
            },
            description: "A list of sessions.",
          },
        },
        additionalProperties: true,
      },
    },
  },
};

export default listSessions;
