import { AppBlock, events } from "@slflows/sdk/v1";
import {
  AthenaClient,
  ListNotebookMetadataCommand,
} from "@aws-sdk/client-athena";
import { STSClient, AssumeRoleCommand } from "@aws-sdk/client-sts";

const listNotebookMetadata: AppBlock = {
  name: "List Notebook Metadata",
  description: `Displays the notebook files for the specified workgroup in paginated format.`,
  inputs: {
    default: {
      config: {
        region: {
          name: "Region",
          description: "AWS region for this operation",
          type: "string",
          required: true,
        },
        assumeRoleArn: {
          name: "Assume Role ARN",
          description:
            "Optional IAM role ARN to assume before executing this operation. If provided, the block will use STS to assume this role and use the temporary credentials.",
          type: "string",
          required: false,
        },
        Filters: {
          name: "Filters",
          description: "Search filter string.",
          type: {
            type: "object",
            properties: {
              Name: {
                type: "string",
              },
            },
            additionalProperties: false,
          },
          required: false,
        },
        NextToken: {
          name: "Next Token",
          description:
            "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.",
          type: "string",
          required: false,
        },
        MaxResults: {
          name: "Max Results",
          description: "Specifies the maximum number of results to return.",
          type: "number",
          required: false,
        },
        WorkGroup: {
          name: "Work Group",
          description:
            "The name of the Spark enabled workgroup to retrieve notebook metadata for.",
          type: "string",
          required: true,
        },
      },
      onEvent: async (input) => {
        const { region, assumeRoleArn, ...commandInput } =
          input.event.inputConfig;

        let credentials = {
          accessKeyId: input.app.config.accessKeyId,
          secretAccessKey: input.app.config.secretAccessKey,
          sessionToken: input.app.config.sessionToken,
        };

        // Determine credentials to use
        if (assumeRoleArn) {
          // Use STS to assume the specified role
          const stsClient = new STSClient({
            region: region,
            credentials: credentials,
            ...(input.app.config.endpoint && {
              endpoint: input.app.config.endpoint,
            }),
          });

          const assumeRoleCommand = new AssumeRoleCommand({
            RoleArn: assumeRoleArn,
            RoleSessionName: `flows-session-${Date.now()}`,
          });

          const assumeRoleResponse = await stsClient.send(assumeRoleCommand);
          credentials = {
            accessKeyId: assumeRoleResponse.Credentials!.AccessKeyId!,
            secretAccessKey: assumeRoleResponse.Credentials!.SecretAccessKey!,
            sessionToken: assumeRoleResponse.Credentials!.SessionToken!,
          };
        }

        const client = new AthenaClient({
          region: region,
          credentials: credentials,
          ...(input.app.config.endpoint && {
            endpoint: input.app.config.endpoint,
          }),
        });

        const command = new ListNotebookMetadataCommand(commandInput as any);
        const response = await client.send(command);

        await events.emit(response || {});
      },
    },
  },
  outputs: {
    default: {
      name: "List Notebook Metadata Result",
      description: "Result from ListNotebookMetadata operation",
      possiblePrimaryParents: ["default"],
      type: {
        type: "object",
        properties: {
          NextToken: {
            type: "string",
            description:
              "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.",
          },
          NotebookMetadataList: {
            type: "array",
            items: {
              type: "object",
              properties: {
                NotebookId: {
                  type: "string",
                },
                Name: {
                  type: "string",
                },
                WorkGroup: {
                  type: "string",
                },
                CreationTime: {
                  type: "string",
                },
                Type: {
                  type: "string",
                },
                LastModifiedTime: {
                  type: "string",
                },
              },
              additionalProperties: false,
            },
            description:
              "The list of notebook metadata for the specified workgroup.",
          },
        },
        additionalProperties: true,
      },
    },
  },
};

export default listNotebookMetadata;
