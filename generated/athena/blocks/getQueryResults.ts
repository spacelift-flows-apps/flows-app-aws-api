import { AppBlock, events } from "@slflows/sdk/v1";
import { AthenaClient, GetQueryResultsCommand } from "@aws-sdk/client-athena";
import { STSClient, AssumeRoleCommand } from "@aws-sdk/client-sts";

const getQueryResults: AppBlock = {
  name: "Get Query Results",
  description: `Streams the results of a single query execution specified by QueryExecutionId from the Athena query results location in Amazon S3.`,
  inputs: {
    default: {
      config: {
        region: {
          name: "Region",
          description: "AWS region for this operation",
          type: "string",
          required: true,
        },
        assumeRoleArn: {
          name: "Assume Role ARN",
          description:
            "Optional IAM role ARN to assume before executing this operation. If provided, the block will use STS to assume this role and use the temporary credentials.",
          type: "string",
          required: false,
        },
        QueryExecutionId: {
          name: "Query Execution Id",
          description: "The unique ID of the query execution.",
          type: "string",
          required: true,
        },
        NextToken: {
          name: "Next Token",
          description:
            "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.",
          type: "string",
          required: false,
        },
        MaxResults: {
          name: "Max Results",
          description:
            "The maximum number of results (rows) to return in this request.",
          type: "number",
          required: false,
        },
        QueryResultType: {
          name: "Query Result Type",
          description:
            "When you set this to DATA_ROWS or empty, GetQueryResults returns the query results in rows.",
          type: "string",
          required: false,
        },
      },
      onEvent: async (input) => {
        const { region, assumeRoleArn, ...commandInput } =
          input.event.inputConfig;

        let credentials = {
          accessKeyId: input.app.config.accessKeyId,
          secretAccessKey: input.app.config.secretAccessKey,
          sessionToken: input.app.config.sessionToken,
        };

        // Determine credentials to use
        if (assumeRoleArn) {
          // Use STS to assume the specified role
          const stsClient = new STSClient({
            region: region,
            credentials: credentials,
            ...(input.app.config.endpoint && {
              endpoint: input.app.config.endpoint,
            }),
          });

          const assumeRoleCommand = new AssumeRoleCommand({
            RoleArn: assumeRoleArn,
            RoleSessionName: `flows-session-${Date.now()}`,
          });

          const assumeRoleResponse = await stsClient.send(assumeRoleCommand);
          credentials = {
            accessKeyId: assumeRoleResponse.Credentials!.AccessKeyId!,
            secretAccessKey: assumeRoleResponse.Credentials!.SecretAccessKey!,
            sessionToken: assumeRoleResponse.Credentials!.SessionToken!,
          };
        }

        const client = new AthenaClient({
          region: region,
          credentials: credentials,
          ...(input.app.config.endpoint && {
            endpoint: input.app.config.endpoint,
          }),
        });

        const command = new GetQueryResultsCommand(commandInput as any);
        const response = await client.send(command);

        await events.emit(response || {});
      },
    },
  },
  outputs: {
    default: {
      name: "Get Query Results Result",
      description: "Result from GetQueryResults operation",
      possiblePrimaryParents: ["default"],
      type: {
        type: "object",
        properties: {
          UpdateCount: {
            type: "number",
            description:
              "The number of rows inserted with a CREATE TABLE AS SELECT, INSERT INTO, or UPDATE statement.",
          },
          ResultSet: {
            type: "object",
            properties: {
              Rows: {
                type: "array",
                items: {
                  type: "object",
                  properties: {
                    Data: {
                      type: "array",
                      items: {
                        type: "object",
                        additionalProperties: true,
                      },
                    },
                  },
                  additionalProperties: false,
                },
              },
              ResultSetMetadata: {
                type: "object",
                properties: {
                  ColumnInfo: {
                    type: "array",
                    items: {
                      type: "object",
                      properties: {
                        CatalogName: {
                          type: "object",
                          additionalProperties: true,
                        },
                        SchemaName: {
                          type: "object",
                          additionalProperties: true,
                        },
                        TableName: {
                          type: "object",
                          additionalProperties: true,
                        },
                        Name: {
                          type: "object",
                          additionalProperties: true,
                        },
                        Label: {
                          type: "object",
                          additionalProperties: true,
                        },
                        Type: {
                          type: "object",
                          additionalProperties: true,
                        },
                        Precision: {
                          type: "object",
                          additionalProperties: true,
                        },
                        Scale: {
                          type: "object",
                          additionalProperties: true,
                        },
                        Nullable: {
                          type: "object",
                          additionalProperties: true,
                        },
                        CaseSensitive: {
                          type: "object",
                          additionalProperties: true,
                        },
                      },
                      required: ["Name", "Type"],
                      additionalProperties: false,
                    },
                  },
                },
                additionalProperties: false,
              },
            },
            additionalProperties: false,
            description: "The results of the query execution.",
          },
          NextToken: {
            type: "string",
            description:
              "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.",
          },
        },
        additionalProperties: true,
      },
    },
  },
};

export default getQueryResults;
